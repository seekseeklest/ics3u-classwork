print("Counting from 0 to 10 by 1:")
i = 0
while i <= 10:
    print(i)
    i += 1

# Count from 10 to 0 by 1
print("\nCounting from 10 to 0 by 1:")
i = 10
while i >= 0:
    print(i)
    i -= 1

